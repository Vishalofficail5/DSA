package Loops;

import java.util.Scanner;

public class PrintN_Ntimes {
    static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        System.out.print("Enter n: ");
        int n = sc.nextInt();

        int i;
        for(i=1; i<=n; i++){
            System.out.println("GOAT");
        }

    }
}
